#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0x0;
	if (nb > 0x0)
	{
		while ((i * i) <= nb)
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	return (0x0);
}
